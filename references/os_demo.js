const os = require('os');

// platform
console.log(os.platform());

// CPU Arch
console.log(os.arch());

// CPU Core INFO
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem()); 

// Home Directory
console.log(os.homedir());

// Uptime
console.log(os.uptime());